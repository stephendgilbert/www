import java.awt.*;
/**
 *  The simplest possible bouncing ball.
 *
 *  @author Stephen Gilbert
 *  @version Mar 20, 2009 11:50:31 AM
 */
public class SimpleBall
{
    /** Radius of the ball */
    public int radius;
    
    /** Location of the ball */
    public Point location;
    
    /** Horizontal velocity of the ball */
    public int dx;
    
    /** Vertical velocity of the ball */
    public int dy;
    
    /** Color of the ball */
    public Color color;
}
